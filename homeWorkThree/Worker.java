package homeWorkThree;

public class Worker {
    private int age;
    private int salary;
    private String name;

    public void setAge(int peopleAge) {
        age = peopleAge;
    }
    public void setSalary(int peopleSalary) {
        salary = peopleSalary;
    }
    public void setName(String peopleName) {
        name = peopleName;
    }

    public int getAge() {
        return age;
    }
    public int getSalary() {
        return salary;
    }
    public String getName() {
        return name;
    }

}
